								<p>TELECOM Nancy est une école d’ingénieur publique créée en 1990, elle appartient au
									collégium Lorraine-INP de l’Université de Lorraine et est affiliée à l’Institut 
									Mines-Télécom. Elle forme des ingénieurs généralistes en informatique, sciences et 
									technologies du numérique en trois ans, le recrutement des élèves se faisant à 
									Bac+2. De plus, elle délivre un diplôme reconnu par le Commission des Titres 
									d’Ingénieur. <br/>
									Les élèves suivant le cursus étudiant se voient proposer cinq 
									approfondissements à partir du deuxième semestre de deuxième année : Ingénierie du 
									Logiciel (IL), Systèmes d’Information d’Entreprise (SIE), Télécommunication, 
									Réseaux et Services (TRS), Logiciel Embarqué (LE) et Ingénierie et Applications des 
									Masses de Données (IAMD).</p>
